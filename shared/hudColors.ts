const DEFAULT_HUD_COLOR_INDEX = 0;

export const parseHexColor = (hex: string): [r: number, g: number, b: number] | null => {
    const sanitized = hex.trim().replace(/^#/, '');
    if (!/^[0-9a-fA-F]{3}$|^[0-9a-fA-F]{6}$/.test(sanitized)) {
        return null;
    }
    const normalized =
        sanitized.length === 3
            ? sanitized
                  .split('')
                  .map((c) => c + c)
                  .join('')
            : sanitized;
    return [
        parseInt(normalized.slice(0, 2), 16),
        parseInt(normalized.slice(2, 4), 16),
        parseInt(normalized.slice(4, 6), 16),
    ];
};

/**
 * Returns the HUD colour index whose RGB is closest to the given hex colour.
 */
export const nearestHudColorIndex = (hex: string): number => {
    const rgb = parseHexColor(hex);
    if (!rgb) return DEFAULT_HUD_COLOR_INDEX;

    const [tr, tg, tb] = rgb;
    let bestIndex = DEFAULT_HUD_COLOR_INDEX;
    let bestDist = Number.POSITIVE_INFINITY;

    for (const [index, r, g, b] of HUD_COLOR_RGB) {
        const dist = (r - tr) ** 2 + (g - tg) ** 2 + (b - tb) ** 2;
        if (dist < bestDist) {
            bestDist = dist;
            bestIndex = index;
        }
    }

    return bestIndex;
};
